static
void CalcEsaPhoDei(const struct EsaPhoDeiTerm *terms,int size,
                   double t,double x[]) {
  x[0] = x[1] = x[2] = 0.0;
/*
  x[3] = x[4] = x[5] = 0.0;
*/
  while (--size >= 0) {
    const double a = (terms->c * t + terms->b) * t;
    const double sa = sin(a);
    const double ca = cos(a);
    x[0] += (terms->s0*sa + terms->c0*ca);
    x[1] += (terms->s1*sa + terms->c1*ca);
    x[2] += (terms->s2*sa + terms->c2*ca);
/*
    const double da = (terms->b + 2*t*terms->c);
    x[3] += (terms->s0*ca - terms->c0*sa) * da;
    x[4] += (terms->s1*ca - terms->c1*sa) * da;
    x[5] += (terms->s2*ca - terms->c2*sa) * da;
*/
    terms++;
  }
}

static
void MultMat(const double a[9],const double b[9],double c[9]) {
  int i,j;
  for (i=0;i<3;i++) {
    for (j=0;j<3;j++) {
      c[i*3+j] = a[i*3]*b[j] + a[i*3+1]*b[3+j] + a[i*3+2]*b[6+j];
    }
  }
}

static
void Mult(const double m[9],double x[3]) {
  const double x0 = x[0];
  const double x1 = x[1];
  const double x2 = x[2];
  x[0] = m[0]*x0 + m[1]*x1 + m[2]*x2;
  x[1] = m[3]*x0 + m[4]*x1 + m[5]*x2;
  x[2] = m[6]*x0 + m[7]*x1 + m[8]*x2;
}


